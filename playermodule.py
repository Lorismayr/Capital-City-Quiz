class Player():
    name = None
    points = 0
    strikes = 0

    # constuctor
    def __init__(self, name):
        name = name.lower()
        name = name.rstrip()
        self.name = name

    # adds point
    def addPoint():
        self.points += 1

    # adds strike
    def addStrike():
        self.strikes += 1
